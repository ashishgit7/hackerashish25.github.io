function houseFeatureCB(){
  var HF =   document.getElementById("HouseFeatureCB").checked
 
  if(HF==false){
     HFinp =   document.getElementById("houseFeatureBox");
     HFinp.style.display = "none"
    }
    else{
        HFinp =   document.getElementById("houseFeatureBox");
     HFinp.style.display = "block"
    }
}
